    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            fb.getFornecedor().setNomeFantasia(tfNomeFantasia.getText().toUpperCase());
            fb.getFornecedor().setRazaoSocial(tfRazaoSocial.getText().toUpperCase());
            fb.getFornecedor().setEndereco(tfEndereco.getText().toUpperCase());
            fb.getFornecedor().setNumero(tfNumero.getText().toUpperCase());
            fb.getFornecedor().setBairro(tfBairro.getText().toUpperCase());
            fb.getFornecedor().setCidade(tfCidade.getText().toUpperCase());
            fb.getFornecedor().setEstado(jcUF.getSelectedItem().toString());
            fb.getFornecedor().setCnpj(tfCnpj.getText());
            fb.getFornecedor().setTelefone(tfTelefone.getText());
            fb.salvar();
            preencherTabela();
            Novo();
            Alertas.mensagemConfirmacao("Fornecedor cadastrado com sucesso!");
        } catch (CampoVazioException e) {
            Alertas.mensagemAviso(e.getMessage());
        } catch (Exception e) {
            Alertas.mensagemErro(e.getMessage());
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton2ActionPerformed

