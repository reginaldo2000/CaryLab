    private void Novo(){
        JTextField tf[] = new JTextField[]{tfNomeFantasia, tfRazaoSocial, tfEndereco, tfNumero, tfBairro, tfCidade, tfCnpj, tfTelefone};
        for(JTextField text : tf){
            text.setText("");
        }
        tfNomeFantasia.requestFocus();
        jcUF.setSelectedIndex(0);
        fb = new FornecedorBean();
    }
    
    private void preencherTabela(){
        DefaultTableModel tb = (DefaultTableModel) jtTabela.getModel();
        tb.setNumRows(0);
        try {
            if (tfPesquisa.getText().isEmpty()) {
                listaFornec = FornecedorDAO.getInstance().listar(new Fornecedor());
            }else{
                listaFornec = FornecedorDAO.getInstance().listar(tfPesquisa.getText());
            }
            for(Fornecedor f : listaFornec){
                tb.addRow(new Object[]{f.getNomeFantasia(), f.getCidade(), f.getTelefone()});
            }
        } catch (Exception e) {
            Alertas.mensagemErro(e.getMessage());
            e.printStackTrace();
        }
    }
