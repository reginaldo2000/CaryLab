    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            sb.getSetor().setNomeSetor(tfNomeSetor.getText().toUpperCase());
            sb.getSetor().setDescricao(tfDescricao.getText().toUpperCase());
            sb.salvar();
            preencherTabela();
            novo();
            Alertas.mensagemConfirmacao("Setor cadastrado com sucesso!");
        } catch (CampoVazioException e) {
            Alertas.mensagemAviso(e.getMessage());
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MousePressed
        int linha = jTable1.getSelectedRow();
        sb.setSetor(listaSetores.get(linha));
        tfNomeSetor.setText(sb.getSetor().getNomeSetor());
        tfDescricao.setText(sb.getSetor().getDescricao());
    }//GEN-LAST:event_jTable1MousePressed

