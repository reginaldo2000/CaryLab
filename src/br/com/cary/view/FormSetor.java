    private void preencherTabela() {
        DefaultTableModel tb = (DefaultTableModel) jTable1.getModel();
        tb.setNumRows(0);
        try {
            listaSetores = SetorDAO.getInstance().listar(new Setor());
            for(Setor s : listaSetores) {
                tb.addRow(new Object[]{s.getNomeSetor(), s.getDescricao()});
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void novo() {
        tfNomeSetor.setText("");
        tfDescricao.setText("");
        tfNomeSetor.requestFocus();
        sb = new SetorBean();
    }
